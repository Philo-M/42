#include <unistd.h>

void	ft_ligne(char a, char b, char c)
{
	write(1, &a, 1);
	write(1, " ", 1);
	write(1, &b, 1);
	write(1, " ", 1);
	write(1, &c, 1);
	write(1, "\n", 1);
}
