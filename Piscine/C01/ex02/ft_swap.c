#include <stdio.h>

void	ft_swap(int *a, int *b)
{
	int	t;

	t = *a;
	*a = *b;
	*b = t;
}

int	main(void)
{
	int	x;
	int	y;

	x = 78;
	y = 25;
	ft_swap(&x, &y);
	printf("x=%d et y=%d", x, y);
	return (0);
}
